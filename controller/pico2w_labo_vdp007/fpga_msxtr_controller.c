// VDP レジスタ書き込み
// reg: レジスタ番号 (0x00〜0x1F など)
// data: 書き込むデータ
static void vdp_write_register(uint8_t reg, uint8_t data) {
	uint8_t buf;

	// 1回目: アドレス 0x99 にデータを送る
	gpio_put(SPI0_CSN_PIN, 0);
	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	spi_write_blocking(SPI0_PORT, &data, 1);
	sleep_us(10);
	// 2回目: アドレス 0x99 にレジスタ番号 | 0x80 を送る
	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = reg | 0x80;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	gpio_put(SPI0_CSN_PIN, 1);
}

// ---------------------------------------------------------------
// VDPレジスタの初期化 (SCREEN 1 モード)
static void vdp_set_screen1( void ) {

	for (int i = 0; i < sizeof(vdp_screen1); i += 2) {
		vdp_write_register(vdp_screen1[i], vdp_screen1[i + 1]);
	}
}

// ---------------------------------------------------------------
static void vdp_set_screen1_font( void ) {
	uint8_t buf;

	gpio_put(SPI0_CSN_PIN, 0);
	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x00;			//	アドレス下位 8bit
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x40 + 0x00;	//	アドレス上位 6bit
	spi_write_blocking(SPI0_PORT, &buf, 1);

	for (int i = 0; i < sizeof(vdp_screen1_font); i++) {
		buf = 0x01;
		spi_write_blocking(SPI0_PORT, &buf, 1);
		buf = 0x98;
		spi_write_blocking(SPI0_PORT, &buf, 1);
		buf = vdp_screen1_font[i];
		spi_write_blocking(SPI0_PORT, &buf, 1);
		sleep_us(10);
	}

	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x00;			//	アドレス下位 8bit
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x01;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x99;
	spi_write_blocking(SPI0_PORT, &buf, 1);
	buf = 0x40 + 0x20;	//	アドレス上位 6bit
	spi_write_blocking(SPI0_PORT, &buf, 1);
	for( int i = 0; i < 32; i++) {
		buf = 0x01;
		spi_write_blocking(SPI0_PORT, &buf, 1);
		buf = 0x98;
		spi_write_blocking(SPI0_PORT, &buf, 1);
		buf = 0xF4;			//	フォントの色 (前景：白、背景：青 )
		spi_write_blocking(SPI0_PORT, &buf, 1);
		sleep_us(10);
	}
	gpio_put(SPI0_CSN_PIN, 1);
}

